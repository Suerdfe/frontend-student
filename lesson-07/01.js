/*
  –ù—É–∂–Ω–æ –Ω–∞–ø–∏—Å–∞—Ç—å —Ñ—É–Ω–∫—Ü–∏—é capitalizeWords, –∫–æ—Ç–æ—Ä–∞—è –ø—Ä–∏–Ω–∏–º–∞–µ—Ç —Å—Ç—Ä–æ–∫—É –∏ –¥–µ–ª–∞–µ—Ç —Ç–∞–∫, —á—Ç–æ–±—ã –∫–∞–∂–¥–æ–µ —Å–ª–æ–≤–æ –Ω–∞—á–∏–Ω–∞–ª–æ—Å—å —Å –∑–∞–≥–ª–∞–≤–Ω–æ–π –±—É–∫–≤—ã.
  –ù–∞–ø—Ä–∏–º–µ—Ä, –µ—Å–ª–∏ –ø–µ—Ä–µ–¥–∞—Ç—å —Å—Ç—Ä–æ–∫—É "hello world from javascript", —Ñ—É–Ω–∫—Ü–∏—è –¥–æ–ª–∂–Ω–∞ –≤–µ—Ä–Ω—É—Ç—å "Hello World From JavaScript".

  üßô‚Äç‚ôÇÔ∏è–≠—Ç—É –∑–∞–¥–∞—á—É –º–æ–∂–Ω–æ —Ä–µ—à–∏—Ç—å –ø—Ä–∏ –ø–æ–º–æ—â–∏ —Ü–∏–∫–ª–∞ for. –†–µ–∫–æ–º–µ–Ω–¥—É–µ–º —Ä–µ–∞–ª–∏–∑–æ–≤–∞—Ç—å —ç—Ç–æ—Ç –≤–∞—Ä–∏–∞–Ω—Ç —Ä–µ—à–µ–Ω–∏—è, –∫–∞–∫ —Å–∞–º—ã–π —ç—Ñ—Ñ–µ–∫—Ç–∏–≤–Ω—ã–π.
  –î—Ä—É–≥–æ–π –≤–∞—Ä–∏–∞–Ω—Ç - –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –º–µ—Ç–æ–¥ split —Å—Ç—Ä–æ–∫, –∏ –º–µ—Ç–æ–¥ –º–∞—Å—Å–∏–≤–∞ join.
*/

function capitalizeWords(str) {
    // –†–∞–∑–±–∏–≤–∞–µ–º –≤—Ö–æ–¥—è—â—É—é —Å—Ç—Ä–æ–∫—É –Ω–∞ –º–∞—Å—Å–∏–≤ —Å–ª–æ–≤
    let words = str.split(' ');

    // –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ –∫–∞–∂–¥–æ–º—É —Å–ª–æ–≤—É –º–∞—Å—Å–∏–≤–∞
    for(let i = 0; i < words.length; i++) {
        // –ë–µ—Ä—ë–º –ø–µ—Ä–≤–æ–µ —Å–∏–º–≤–æ–ª —Å–ª–æ–≤–∞ –∏ –¥–µ–ª–∞–µ–º –µ–≥–æ –∑–∞–≥–ª–∞–≤–Ω—ã–º,
        // –¥–æ–±–∞–≤–ª—è–µ–º –∫ –Ω–µ–º—É –æ—Å—Ç–∞–ª—å–Ω—É—é —á–∞—Å—Ç—å —Å–ª–æ–≤–∞ (–Ω–∞—á–∏–Ω–∞—è —Å–æ –≤—Ç–æ—Ä–æ–≥–æ —Å–∏–º–≤–æ–ª–∞)
        words[i] = words[i][0].toUpperCase() + words[i].slice(1);
    }

    // –°–æ–±–∏—Ä–∞–µ–º –æ–±—Ä–∞—Ç–Ω–æ —Å—Ç—Ä–æ–∫—É –∏–∑ –æ–±—Ä–∞–±–æ—Ç–∞–Ω–Ω—ã—Ö —Å–ª–æ–≤
    return words.join(' ');
}

